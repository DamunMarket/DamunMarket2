def pick(pool, date_str, zodiac, field):
    key = f"{date_str}-{zodiac}-{field}".encode("utf-8")
    idx = int(hashlib.sha256(key).hexdigest(), 16) % len(pool)
    return pool[idx]


def pick_int(date_str, zodiac, field, lo, hi):
    key = f"{date_str}-{zodiac}-{field}".encode("utf-8")
    return lo + int(hashlib.sha256(key).hexdigest(), 16) % (hi - lo + 1)


def main():
    now = datetime.now(KST)
    date_str = now.strftime("%Y-%m-%d")

    items = []
    for name, emoji, years in ZODIACS:
        items.append({
            "zodiac": name,
            "emoji": emoji,
            "years": years,
            "score": pick_int(date_str, name, "score", 2, 5),   # 별 2~5개
            "summary": pick(SUMMARY, date_str, name, "summary"),
            "love": pick(LOVE, date_str, name, "love"),
            "money": pick(MONEY, date_str, name, "money"),
            "health": pick(HEALTH, date_str, name, "health"),
            "advice": pick(ADVICE, date_str, name, "advice"),
            "luckyNumber": pick_int(date_str, name, "num", 1, 45),
            "luckyColor": pick(COLORS, date_str, name, "color"),
        })

    output = {
        "updatedAt": date_str,
        "updatedAtIso": now.isoformat(),
        "note": "재미로 보는 오늘의 운세입니다. 날짜별로 매일 바뀝니다.",
        "items": items,
    }

    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    OUTPUT_PATH.write_text(json.dumps(output, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"저장 완료: {OUTPUT_PATH} ({len(items)}개 띠)")


if __name__ == "__main__":
    main()
